const calculator = {
  plus: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  div: function (a, b) {
    console.log(a / b);
  },
  power: function (a, b) {
    console.log(a ** b);
  },
};
calculator.plus(5, 1);
calculator.minus(5, 1);
calculator.div(5, 1);
calculator.power(5, 1);
